public class RelationalExpressions {
    public static void main(String[] args) {
        System.out.println(1 == 1);
        System.out.println(1 == 2);
        System.out.println(1 != 1);
        System.out.println(1 != 2);
        System.out.println(1 >= 1);
        System.out.println(1 >= 2);
        System.out.println(1 <= 1);
        System.out.println(1 <= 2);
        System.out.println(1 > 1);
        System.out.println(1 > 2);
        System.out.println(1 < 1);
        System.out.println(1 < 2);


    }
}
