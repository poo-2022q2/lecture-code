public class ArithmeticExpressions {
    public static void main(String[] args) {
        System.out.println(5 + 3);
        System.out.println(5 - 3);
        System.out.println(5 * 3);
        System.out.println(5 / 3);
        System.out.println(5 % 3);
        System.out.println();

        System.out.println(5L + 3);
        System.out.println(5L - 3);
        System.out.println(5L * 3);
        System.out.println(5L / 3);
        System.out.println(5L % 3);
        System.out.println();

        // type promotion
        System.out.println(5.0 + 3);
        System.out.println(5.0 - 3);
        System.out.println(5.0 * 3);
        System.out.println(5.0 / 3);
        System.out.println(5.0 % 3);
        System.out.println();

        System.out.println(5.0f + 3);
        System.out.println(5.0f - 3);
        System.out.println(5.0f * 3);
        System.out.println(5.0f / 3);
        System.out.println(5.0f % 3);
        System.out.println();
    }
}
